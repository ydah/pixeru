# Usage:
#   ruby tools/font_generator.rb output.rb
#   ruby tools/font_generator.rb output.rb --from-bmp=font_sheet.bmp --char-width=5 --char-height=7

OUTPUT_PATH = ARGV[0]
unless OUTPUT_PATH
  puts "Usage: ruby tools/font_generator.rb output.rb [--from-bmp=sheet.bmp --char-width=5 --char-height=7]"
  exit(1)
end

char_width = 5
char_height = 7
bmp_path = nil

ARGV[1..].each do |arg|
  if arg.start_with?("--from-bmp=")
    bmp_path = arg.sub("--from-bmp=", "")
  elsif arg.start_with?("--char-width=")
    char_width = arg.sub("--char-width=", "").to_i
  elsif arg.start_with?("--char-height=")
    char_height = arg.sub("--char-height=", "").to_i
  end
end

if bmp_path
  data = File.binread(bmp_path)
  raise "Not a BMP file" unless data[0, 2] == "BM"

  offset = data[10, 4].unpack("V")[0]
  img_width = data[18, 4].unpack("V")[0]
  img_height = data[22, 4].unpack("V")[0]
  bpp = data[28, 2].unpack("v")[0]
  raise "Only 24-bit BMP supported" unless bpp == 24

  row_size = ((img_width * 3 + 3) / 4) * 4
  pixels = Array.new(img_width * img_height, false)

  (0...img_height).each do |y|
    src_y = img_height - 1 - y
    (0...img_width).each do |x|
      idx = offset + src_y * row_size + x * 3
      b = data.getbyte(idx)
      g = data.getbyte(idx + 1)
      r = data.getbyte(idx + 2)
      pixels[y * img_width + x] = (r + g + b) < 384
    end
  end

  cols_per_row = img_width / char_width
  rows_count = img_height / char_height

  File.open(OUTPUT_PATH, "w") do |f|
    f.puts "module Pixeru"
    f.puts "  module FontData"
    f.puts "    CUSTOM = {"

    char_code = 32
    (0...rows_count).each do |cr|
      (0...cols_per_row).each do |cc|
        break if char_code > 126
        bytes = []
        (0...char_width).each do |col|
          byte = 0
          (0...char_height).each do |row|
            px = cc * char_width + col
            py = cr * char_height + row
            if pixels[py * img_width + px]
              byte |= (1 << row)
            end
          end
          bytes << byte
        end
        f.puts "      #{char_code} => [#{bytes.map { |b| "0x%02X" % b }.join(", ")}],"
        char_code += 1
      end
    end

    f.puts "    }"
    f.puts "  end"
    f.puts "end"
  end
